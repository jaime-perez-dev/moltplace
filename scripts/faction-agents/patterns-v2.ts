// Get random pattern for faction
export function getRandomPatternV2(factionSlug: string): { name: string; pattern: number[][]; colors: Record<number, string> } {
  const patterns = PATTERNS_V2[factionSlug as keyof typeof PATTERNS_V2];
  const colors = FACTION_COLORS[factionSlug as keyof typeof FACTION_COLORS];
  
  if (!patterns || !colors) {
    return { name: "block", pattern: [[1,1],[1,1]], colors: { 1: "#FF0000" } };
  }
  
  const names = Object.keys(patterns);
  const name = names[Math.floor(Math.random() * names.length)];
  const pattern = patterns[name as keyof typeof patterns];
  
  return { name, pattern, colors };
}
